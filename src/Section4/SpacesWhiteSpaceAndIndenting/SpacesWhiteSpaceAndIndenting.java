package Section4.SpacesWhiteSpaceAndIndenting;

public class SpacesWhiteSpaceAndIndenting {

    public static void main(String[] args) {

        int myVariable = 50; // The entire line is a statement
        myVariable++; // Also a complete statement
        myVariable--;
        System.out.println("This is a test");

        System.out.println("This is " +
                "another " +
                "still more."); //Still, one statement on several lines


    }
}
